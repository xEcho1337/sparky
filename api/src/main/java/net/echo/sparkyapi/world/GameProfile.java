package net.echo.sparkyapi.world;

import java.util.UUID;

public class GameProfile {

    private String name;
    private UUID uuid;

    public GameProfile(String name, UUID uuid) {
        this.name = name;
        this.uuid = uuid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public UUID getUuid() {
        return uuid;
    }

    public void setUuid(UUID uuid) {
        this.uuid = uuid;
    }
}
